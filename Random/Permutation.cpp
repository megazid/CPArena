#define ll unsigned long long
#define UWU ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
const ll MOD = 1e9 + 7; 

bool can(vector<int> vec,int n){
    for(int i=1;i<n;i++){
        if(abs(vec[i]-vec[i-1])%i!=0)return false;
    }
    return true;
}


void solve1(){
    int n;cin>>n;
    vector<int> vec(n);
    for(int i=0;i<n;i++)vec[i]=i+1;
    
    do{
        if(can(vec,n)){
            for(int it:vec)cout<<it<<" ";
            cout<<"\n";
        }
    }
    while(next_permutation(vec.begin(),vec.end()));
}

void solve(){
    int n;cin>>n;
    int l=n/2;
    int r=(n/2)+1;
    if(n%2!=0)cout<<r++<<" ";
    while(l>0)
        cout<<l--<<" "<<r++<<" ";
    cout<<"\n";

    
}
 
 
int main(){
    UWU;
    int tc=1;
    cin>>tc;
    while(tc--){
        solve(); 
    }
 
    return 0;
}


